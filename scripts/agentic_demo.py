class OpenAICompatibleClient:
    """
    Supports OpenAI-compatible chat-completions providers.

    Environment variables:

        NOVA_LLM_API_KEY
        NOVA_LLM_BASE_URL
        NOVA_LLM_MODEL

    OPENAI_* variables are accepted as fallbacks.
    """

    def __init__(
        self,
        api_key: Optional[str] = None,
        base_url: Optional[str] = None,
        model: Optional[str] = None,
        timeout: float = 60.0,
    ) -> None:
        self.api_key = (
            api_key
            or os.getenv("NOVA_LLM_API_KEY")
            or os.getenv("OPENAI_API_KEY")
        )

        self.base_url = (
            base_url
            or os.getenv("NOVA_LLM_BASE_URL")
            or os.getenv("OPENAI_BASE_URL")
            or "https://api.openai.com/v1"
        ).rstrip("/")

        self.model = (
            model
            or os.getenv("NOVA_LLM_MODEL")
            or os.getenv("OPENAI_MODEL")
            or "gpt-4.1-mini"
        )

        self.timeout = timeout

    @property
    def configured(self) -> bool:
        return bool(
            self.api_key
            and self.model
            and self.base_url
        )

    def complete(
        self,
        *,
        system: str,
        user: str,
        temperature: float = 0.1,
        max_tokens: int = 2500,
        json_mode: bool = False,
    ) -> str:
        if not self.configured:
            raise RuntimeError(
                "LLM is not configured"
            )

        payload: dict[str, Any] = {
            "model": self.model,
            "messages": [
                {
                    "role": "system",
                    "content": system,
                },
                {
                    "role": "user",
                    "content": user,
                },
            ],
            "temperature": temperature,
            "max_tokens": max_tokens,
        }

        if json_mode:
            payload["response_format"] = {
                "type": "json_object"
            }

        response = _json_request(
            f"{self.base_url}/chat/completions",
            method="POST",
            headers={
                "Authorization": (
                    f"Bearer {self.api_key}"
                )
            },
            payload=payload,
            timeout=self.timeout,
        )

        choices = response.get("choices") or []

        if not choices:
            raise RuntimeError(
                "LLM response contained no choices"
            )

        content = (
            choices[0]
            .get("message", {})
            .get("content")
        )

        if not isinstance(content, str):
            raise RuntimeError(
                "LLM response contained invalid content"
            )

        content = content.strip()

        if not content:
            raise RuntimeError(
                "LLM response contained empty content"
            )

        return content


