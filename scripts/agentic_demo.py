class Status(str, Enum):
    PENDING = "pending"
    RUNNING = "running"
    DONE = "done"
    FAILED = "failed"
    BLOCKED = "blocked"


@dataclass(slots=True)
class ToolResult:
    """
    Standard result envelope returned by every tool.

    ok:
        Whether the tool successfully completed its operation.

    data:
        The real output produced by the tool.

    confidence:
        Evidence-coverage heuristic. This is not a declaration of factual truth.

    evidence:
        Evidence records generated by the tool.

    error:
        Explicit failure reason.

    retryable:
        Whether retrying the same operation may reasonably succeed.

    metadata:
        Additional machine-readable execution information.
    """

    ok: bool
    data: JSONValue = None
    confidence: float = 0.0
    evidence: list[dict[str, Any]] = field(default_factory=list)
    error: Optional[str] = None
    retryable: bool = False
    metadata: dict[str, Any] = field(default_factory=dict)

    @classmethod
    def success(
        cls,
        data: JSONValue,
        *,
        confidence: float = 1.0,
        evidence: Optional[list[dict[str, Any]]] = None,
        metadata: Optional[dict[str, Any]] = None,
    ) -> "ToolResult":
        return cls(
            ok=True,
            data=data,
            confidence=max(0.0, min(1.0, float(confidence))),
            evidence=evidence or [],
            metadata=metadata or {},
        )

    @classmethod
    def failure(
        cls,
        error: str,
        *,
        retryable: bool = False,
        metadata: Optional[dict[str, Any]] = None,
    ) -> "ToolResult":
        return cls(
            ok=False,
            error=error,
            retryable=retryable,
            metadata=metadata or {},
        )


