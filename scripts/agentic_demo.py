def _build_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(
        description=(
            "NOVA evidence-oriented "
            "research agent MVP"
        )
    )

    parser.add_argument(
        "--goal",
        required=True,
        help="Research objective",
    )

    parser.add_argument(
        "--output",
        default="nova_report.md",
        help=(
            "Path for the final "
            "Markdown report"
        ),
    )

    parser.add_argument(
        "--summary-json",
        default="nova_run.json",
        help=(
            "Path for the machine-readable "
            "execution report"
        ),
    )

    parser.add_argument(
        "--memory-db",
        default=".nova_memory.sqlite3",
        help="SQLite memory path",
    )

    parser.add_argument(
        "--max-parallel",
        type=int,
        default=4,
        help=(
            "Maximum number of independent "
            "steps to execute concurrently"
        ),
    )

    parser.add_argument(
        "--plan-only",
        action="store_true",
        help=(
            "Generate and print the plan "
            "without executing it"
        ),
    )

    parser.add_argument(
        "--verbose",
        action="store_true",
        help=(
            "Print operational execution logs"
        ),
    )

    return parser


def main(
    argv: Optional[list[str]] = None,
) -> int:
    args = _build_parser().parse_args(
        argv
    )

    logging.basicConfig(
        level=(
            logging.INFO
            if args.verbose
            else logging.WARNING
        ),
        format=(
            "%(asctime)s "
            "%(levelname)s "
            "%(message)s"
        ),
    )

    with AgenticAI(
        memory_path=args.memory_db,
        max_parallel=args.max_parallel,
    ) as agent:
        agent.plan(args.goal)

        if args.plan_only:
            plan_payload = [
                dataclasses.asdict(step)
                for step in agent.plan_steps
            ]

            print(
                json.dumps(
                    plan_payload,
                    indent=2,
                    default=str,
                )
            )

            return 0

        success = agent.execute()

        if agent.last_report is None:
            raise RuntimeError(
                "Execution ended without a run report"
            )

        output_path = Path(
            args.output
        )

        summary_path = Path(
            args.summary_json
        )

        output_path.write_text(
            agent.last_report.final_output,
            encoding="utf-8",
        )

        summary_path.write_text(
            json.dumps(
                agent.last_report.to_dict(),
                ensure_ascii=False,
                indent=2,
            ),
            encoding="utf-8",
        )

        print(
            json.dumps(
                {
                    "success": (
                        agent.last_report.success
                    ),
                    "health": (
                        agent.last_report.health
                    ),
                    "run_id": (
                        agent.last_report.run_id
                    ),
                    "report": str(
                        output_path.resolve()
                    ),
                    "run_summary": str(
                        summary_path.resolve()
                    ),
                },
                indent=2,
            )
        )

        return (
            0
            if success
            else 1
        )


if __name__ == "__main__":
    raise SystemExit(main())
