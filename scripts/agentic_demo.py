class AgenticAI:
    """
    Evidence-oriented research agent.

    Interface:

        agent.plan(objective)
        agent.execute()

    The runtime executes a dependency graph, retries explicitly retryable
    operations, blocks steps whose dependencies fail, verifies the final report,
    persists execution evidence, and returns failure honestly.
    """

    def __init__(
        self,
        name: str = "NOVA-Agent",
        *,
        memory_path: str | Path = ".nova_memory.sqlite3",
        max_parallel: int = 4,
        tools: Optional[
            dict[str, ToolCallable]
        ] = None,
        llm: Optional[
            OpenAICompatibleClient
        ] = None,
        retry_base_seconds: float = 0.75,
    ) -> None:
        self.name = name

        self.max_parallel = max(
            1,
            int(max_parallel),
        )

        self.retry_base_seconds = max(
            0.0,
            float(retry_base_seconds),
        )

        self.memory = SQLiteMemory(
            memory_path
        )

        self.llm = (
            llm
            or OpenAICompatibleClient()
        )

        self.tools = default_tool_registry()

        if tools:
            self.tools.update(tools)

        self.goal: Optional[str] = None
        self.plan_steps: list[Step] = []

        self.last_report: Optional[
            RunReport
        ] = None

        self._run_id = ""
        self._results: dict[str, Any] = {}
        self._events: list[
            dict[str, Any]
        ] = []

        self._lock = threading.RLock()

    def close(self) -> None:
        self.memory.close()

    def __enter__(self) -> "AgenticAI":
        return self

    def __exit__(
        self,
        exc_type: Any,
        exc: Any,
        traceback: Any,
    ) -> None:
        del exc_type
        del exc
        del traceback

        self.close()

    def _emit(
        self,
        event: str,
        message: str,
        details: Optional[
            dict[str, Any]
        ] = None,
    ) -> None:
        item = {
            "ts": dt.datetime.now(
                dt.timezone.utc
            ).isoformat(),
            "event": event,
            "message": message,
            "details": details or {},
        }

        with self._lock:
            self._events.append(item)

        LOGGER.info(
            "%s: %s",
            event,
            message,
        )

    def plan(
        self,
        objective: str,
    ) -> bool:
        objective = str(
            objective or ""
        ).strip()

        if not objective:
            raise ValueError(
                "objective must not be empty"
            )

        self.goal = objective

        current_year = dt.datetime.now(
            dt.timezone.utc
        ).year

        safe_key = re.sub(
            r"[^a-zA-Z0-9_.-]+",
            "_",
            objective.lower(),
        ).strip("_")[:80]

        self.plan_steps = [
            Step(
                id="R1",
                description=(
                    "Gather current web evidence"
                ),
                tool="web_research",
                args={
                    "queries": [
                        objective,
                        (
                            f"{objective} "
                            "latest developments "
                            f"{current_year}"
                        ),
                    ],
                    "max_results": 6,
                },
                max_retries=2,
            ),
            Step(
                id="R2",
                description=(
                    "Fetch and extract "
                    "the strongest sources"
                ),
                tool="fetch_sources",
                args={
                    "research": "__R1__",
                    "max_pages": 4,
                },
                depends_on=["R1"],
                max_retries=1,
            ),
            Step(
                id="R3",
                description=(
                    "Create a source-grounded report"
                ),
                tool="synthesize_report",
                args={
                    "goal": objective,
                    "research": "__R1__",
                    "fetched": "__R2__",
                },
                depends_on=[
                    "R1",
                    "R2",
                ],
                max_retries=2,
            ),
            Step(
                id="R4",
                description=(
                    "Verify citations, completeness, "
                    "and evidence grounding"
                ),
                tool="verify_report",
                args={
                    "report": "__R3__",
                    "research": "__R1__",
                    "fetched": "__R2__",
                    "llm_used": self.llm.configured,
                },
                depends_on=[
                    "R1",
                    "R2",
                    "R3",
                ],
                max_retries=0,
            ),
            Step(
                id="R5",
                description=(
                    "Finalize or revise "
                    "the verified report"
                ),
                tool="revise_report",
                args={
                    "goal": objective,
                    "report": "__R3__",
                    "verification": "__R4__",
                    "research": "__R1__",
                    "fetched": "__R2__",
                    "llm_used": self.llm.configured,
                },
                depends_on=[
                    "R1",
                    "R2",
                    "R3",
                    "R4",
                ],
                max_retries=1,
                is_final=True,
            ),
            Step(
                id="R6",
                description=(
                    "Persist the final report"
                ),
                tool="persist_result",
                args={
                    "key": (
                        "research_report_"
                        f"{safe_key}"
                    ),
                    "value": "__R5__",
                },
                depends_on=["R5"],
                max_retries=1,
            ),
        ]

        self.validate_plan()

        return True

    def validate_plan(self) -> None:
        if not self.plan_steps:
            raise PlanValidationError(
                "Plan has no steps"
            )

        ids = [
            step.id
            for step in self.plan_steps
        ]

        if len(ids) != len(set(ids)):
            raise PlanValidationError(
                "Step IDs must be unique"
            )

        by_id = {
            step.id: step
            for step in self.plan_steps
        }

        final_steps = [
            step
            for step in self.plan_steps
            if step.is_final
        ]

        if len(final_steps) != 1:
            raise PlanValidationError(
                (
                    "Plan must contain exactly "
                    "one final step"
                )
            )

        for step in self.plan_steps:
            if step.tool not in self.tools:
                raise PlanValidationError(
                    (
                        f"Step {step.id} references "
                        f"unknown tool {step.tool!r}"
                    )
                )

            if step.id in step.depends_on:
                raise PlanValidationError(
                    (
                        f"Step {step.id} "
                        "depends on itself"
                    )
                )

            missing = [
                dependency
                for dependency in step.depends_on
                if dependency not in by_id
            ]

            if missing:
                raise PlanValidationError(
                    (
                        f"Step {step.id} has missing "
                        f"dependencies: {missing}"
                    )
                )

        visiting: set[str] = set()
        visited: set[str] = set()

        def visit(
            step_id: str,
        ) -> None:
            if step_id in visiting:
                raise PlanValidationError(
                    (
                        "Dependency cycle "
                        f"detected at {step_id}"
                    )
                )

            if step_id in visited:
                return

            visiting.add(step_id)

            for dependency in (
                by_id[step_id].depends_on
            ):
                visit(dependency)

            visiting.remove(step_id)
            visited.add(step_id)

        for step_id in ids:
            visit(step_id)

    def _resolve_value(
        self,
        value: Any,
    ) -> Any:
        if isinstance(value, str):
            match = re.fullmatch(
                r"__(.+)__",
                value,
            )

            if match:
                reference = match.group(1)

                if reference not in self._results:
                    raise KeyError(
                        (
                            "Unresolved step reference: "
                            f"{reference}"
                        )
                    )

                return self._results[reference]

            return value

        if isinstance(value, list):
            return [
                self._resolve_value(item)
                for item in value
            ]

        if isinstance(value, dict):
            return {
                key: self._resolve_value(item)
                for key, item in value.items()
            }

        return value

    def _run_step(
        self,
        step: Step,
        context: ToolContext,
    ) -> None:
        with self._lock:
            step.status = Status.RUNNING
            step.started_at = time.monotonic()

        self._emit(
            "step_started",
            step.description,
            {
                "step_id": step.id,
                "tool": step.tool,
            },
        )

        try:
            args = self._resolve_value(
                step.args
            )

        except KeyError as exc:
            result = ToolResult.failure(
                str(exc),
                retryable=False,
            )

            with self._lock:
                step.result = result
                step.status = Status.FAILED
                step.ended_at = time.monotonic()

            self._emit(
                "step_failed",
                str(exc),
                {
                    "step_id": step.id,
                },
            )

            return

        tool = self.tools[step.tool]
        max_attempts = step.max_retries + 1

        for attempt in range(
            1,
            max_attempts + 1,
        ):
            step.attempts = attempt

            try:
                result = tool(
                    args,
                    context,
                )

                if not isinstance(
                    result,
                    ToolResult,
                ):
                    raise TypeError(
                        (
                            f"Tool {step.tool!r} returned "
                            f"{type(result).__name__}; "
                            "expected ToolResult"
                        )
                    )

            except Exception as exc:
                result = ToolResult.failure(
                    (
                        "Unhandled tool exception: "
                        f"{type(exc).__name__}: {exc}"
                    ),
                    retryable=False,
                )

            if result.ok:
                with self._lock:
                    step.result = result
                    step.status = Status.DONE
                    step.ended_at = time.monotonic()

                    self._results[step.id] = (
                        result.data
                    )

                self.memory.store(
                    self._run_id,
                    f"step:{step.id}",
                    {
                        "tool": step.tool,
                        "confidence": result.confidence,
                        "metadata": result.metadata,
                        "data": result.data,
                    },
                )

                self._emit(
                    "step_completed",
                    step.description,
                    {
                        "step_id": step.id,
                        "attempt": attempt,
                        "confidence": (
                            result.confidence
                        ),
                    },
                )

                return

            can_retry = bool(
                result.retryable
                and attempt < max_attempts
            )

            self._emit(
                "step_attempt_failed",
                (
                    result.error
                    or "Tool failed"
                ),
                {
                    "step_id": step.id,
                    "attempt": attempt,
                    "retrying": can_retry,
                },
            )

            if can_retry:
                delay = (
                    self.retry_base_seconds
                    * (2 ** (attempt - 1))
                )

                delay += random.uniform(
                    0.0,
                    (
                        self.retry_base_seconds
                        * 0.2
                    ),
                )

                time.sleep(delay)
                continue

            with self._lock:
                step.result = result
                step.status = Status.FAILED
                step.ended_at = time.monotonic()

            self._emit(
                "step_failed",
                (
                    result.error
                    or "Tool failed"
                ),
                {
                    "step_id": step.id,
                    "attempts": attempt,
                },
            )

            return

    def _block_dependents(
        self,
    ) -> bool:
        changed = False

        by_id = {
            step.id: step
            for step in self.plan_steps
        }

        for step in self.plan_steps:
            if step.status != Status.PENDING:
                continue

            failed_dependencies = [
                dependency
                for dependency in step.depends_on
                if by_id[dependency].status
                in {
                    Status.FAILED,
                    Status.BLOCKED,
                }
            ]

            if not failed_dependencies:
                continue

            step.status = Status.BLOCKED

            step.result = ToolResult.failure(
                (
                    "Blocked by failed dependencies: "
                    + ", ".join(
                        failed_dependencies
                    )
                )
            )

            changed = True

            self._emit(
                "step_blocked",
                (
                    step.result.error
                    or "Step blocked"
                ),
                {
                    "step_id": step.id,
                    "dependencies": (
                        failed_dependencies
                    ),
                },
            )

        return changed

    def _dependency_status(
        self,
        dependency_id: str,
    ) -> Status:
        for step in self.plan_steps:
            if step.id == dependency_id:
                return step.status

        raise KeyError(
            (
                "Dependency does not exist: "
                f"{dependency_id}"
            )
        )

    def execute(self) -> bool:
        if not self.goal:
            raise RuntimeError(
                (
                    "No goal exists. "
                    "Call plan(objective) first."
                )
            )

        if not self.plan_steps:
            raise RuntimeError(
                (
                    "No plan exists. "
                    "Call plan(objective) first."
                )
            )

        self.validate_plan()

        self._run_id = uuid.uuid4().hex
        self._results = {}
        self._events = []
        self.last_report = None

        for step in self.plan_steps:
            step.status = Status.PENDING
            step.result = None
            step.attempts = 0
            step.started_at = None
            step.ended_at = None

        started_wall = dt.datetime.now(
            dt.timezone.utc
        )

        started_monotonic = time.monotonic()

        context = ToolContext(
            run_id=self._run_id,
            goal=self.goal,
            memory=self.memory,
            llm=self.llm,
            emit=self._emit,
        )

        self._emit(
            "run_started",
            (
                f"Executing "
                f"{len(self.plan_steps)} steps"
            ),
            {
                "run_id": self._run_id,
                "goal": self.goal,
            },
        )

        while True:
            self._block_dependents()

            ready = [
                step
                for step in self.plan_steps
                if (
                    step.status
                    == Status.PENDING
                )
                and all(
                    (
                        self._dependency_status(
                            dependency
                        )
                        == Status.DONE
                    )
                    for dependency
                    in step.depends_on
                )
            ]

            if not ready:
                terminal = all(
                    step.status
                    in {
                        Status.DONE,
                        Status.FAILED,
                        Status.BLOCKED,
                    }
                    for step in self.plan_steps
                )

                if terminal:
                    break

                pending = [
                    step.id
                    for step in self.plan_steps
                    if (
                        step.status
                        == Status.PENDING
                    )
                ]

                for step in self.plan_steps:
                    if (
                        step.status
                        == Status.PENDING
                    ):
                        step.status = Status.BLOCKED

                        step.result = ToolResult.failure(
                            "Scheduler made no progress"
                        )

                self._emit(
                    "scheduler_stalled",
                    "No runnable steps remained",
                    {
                        "pending": pending,
                    },
                )

                break

            batch = ready[
                :self.max_parallel
            ]

            with ThreadPoolExecutor(
                max_workers=len(batch)
            ) as pool:
                futures = [
                    pool.submit(
                        self._run_step,
                        step,
                        context,
                    )
                    for step in batch
                ]

                for future in as_completed(
                    futures
                ):
                    future.result()

        ended_wall = dt.datetime.now(
            dt.timezone.utc
        )

        elapsed = (
            time.monotonic()
            - started_monotonic
        )

        final_step = next(
            step
            for step in self.plan_steps
            if step.is_final
        )

        if (
            final_step.status == Status.DONE
            and final_step.result is not None
        ):
            final_output = str(
                final_step.result.data
            )
        else:
            final_output = ""

        failed = [
            step
            for step in self.plan_steps
            if step.status == Status.FAILED
        ]

        blocked = [
            step
            for step in self.plan_steps
            if step.status == Status.BLOCKED
        ]

        verification_passed = bool(
            final_step.result
            and final_step.result.metadata.get(
                "verification_passed",
                False,
            )
        )

        success = bool(
            not failed
            and not blocked
            and final_step.status == Status.DONE
        )

        if (
            success
            and verification_passed
        ):
            health = "GOOD"

        elif final_step.status == Status.DONE:
            health = "DEGRADED"

        else:
            health = "FAILED"

        evidence: list[
            dict[str, Any]
        ] = []

        seen_evidence: set[
            tuple[Any, ...]
        ] = set()

        for step in self.plan_steps:
            if not step.result:
                continue

            for item in step.result.evidence:
                fingerprint = (
                    item.get("type"),
                    item.get("url"),
                    item.get("sha256"),
                    item.get("title"),
                )

                if fingerprint in seen_evidence:
                    continue

                seen_evidence.add(fingerprint)
                evidence.append(item)

        step_reports: list[
            dict[str, Any]
        ] = []

        for step in self.plan_steps:
            duration: Optional[float] = None

            if (
                step.started_at is not None
                and step.ended_at is not None
            ):
                duration = round(
                    (
                        step.ended_at
                        - step.started_at
                    ),
                    3,
                )

            step_reports.append(
                {
                    "id": step.id,
                    "description": (
                        step.description
                    ),
                    "tool": step.tool,
                    "status": (
                        step.status.value
                    ),
                    "attempts": step.attempts,
                    "confidence": (
                        step.result.confidence
                        if step.result
                        else 0.0
                    ),
                    "error": (
                        step.result.error
                        if step.result
                        else None
                    ),
                    "metadata": (
                        step.result.metadata
                        if step.result
                        else {}
                    ),
                    "duration_seconds": duration,
                }
            )

        self._emit(
            "run_completed",
            (
                "Run finished with "
                f"health={health}"
            ),
            {
                "success": success,
                "failed_steps": [
                    step.id
                    for step in failed
                ],
                "blocked_steps": [
                    step.id
                    for step in blocked
                ],
                "elapsed_seconds": round(
                    elapsed,
                    3,
                ),
            },
        )

        self.last_report = RunReport(
            run_id=self._run_id,
            goal=self.goal,
            success=success,
            health=health,
            final_output=final_output,
            started_at=(
                started_wall.isoformat()
            ),
            ended_at=(
                ended_wall.isoformat()
            ),
            elapsed_seconds=round(
                elapsed,
                3,
            ),
            steps=step_reports,
            events=list(self._events),
            evidence=evidence,
        )

        self.memory.store(
            self._run_id,
            "run_report",
            self.last_report.to_dict(),
        )

        return success


