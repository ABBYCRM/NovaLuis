def _confidence_from_evidence(
    source_count: int,
    page_count: int = 0,
) -> float:
    """
    Transparent evidence-coverage heuristic.

    It does not claim the answer is factually true.
    """

    return min(
        0.95,
        (
            0.30
            + min(source_count, 8) * 0.06
            + min(page_count, 5) * 0.04
        ),
    )


def tool_web_research(
    args: dict[str, Any],
    ctx: ToolContext,
) -> ToolResult:
    del ctx

    queries = args.get("queries")

    if isinstance(queries, str):
        queries = [queries]

    if (
        not isinstance(queries, list)
        or not all(
            isinstance(query, str)
            for query in queries
        )
    ):
        return ToolResult.failure(
            "'queries' must be a string "
            "or list of strings"
        )

    cleaned_queries = [
        query.strip()
        for query in queries
        if query.strip()
    ]

    if not cleaned_queries:
        return ToolResult.failure(
            "No valid search queries were supplied"
        )

    try:
        max_results = int(
            args.get("max_results", 6)
        )
    except (
        TypeError,
        ValueError,
    ):
        return ToolResult.failure(
            "'max_results' must be an integer"
        )

    max_results = max(
        1,
        min(max_results, 10),
    )

    provider = (
        "tavily"
        if os.getenv("TAVILY_API_KEY")
        else "duckduckgo"
    )

    all_sources: list[dict[str, Any]] = []
    errors: list[str] = []

    for query in cleaned_queries:
        try:
            if provider == "tavily":
                sources = _search_tavily(
                    query,
                    max_results,
                )
            else:
                sources = _search_duckduckgo(
                    query,
                    max_results,
                )

            for source in sources:
                source["query"] = query
                source["provider"] = provider

            all_sources.extend(sources)

        except (
            urllib.error.URLError,
            TimeoutError,
            RuntimeError,
            ValueError,
        ) as exc:
            errors.append(
                f"{query}: {exc}"
            )

    unique = _normalize_sources(
        all_sources
    )

    if not unique:
        return ToolResult.failure(
            (
                "All web searches failed or "
                "returned zero results. "
                + "; ".join(errors)
            ),
            retryable=True,
            metadata={
                "provider": provider,
                "queries": cleaned_queries,
            },
        )

    data = {
        "queries": cleaned_queries,
        "provider": provider,
        "sources": unique,
        "partial_errors": errors,
        "retrieved_at": dt.datetime.now(
            dt.timezone.utc
        ).isoformat(),
    }

    evidence = [
        {
            "type": "search_result",
            "title": source["title"],
            "url": source["url"],
        }
        for source in unique
    ]

    return ToolResult.success(
        data,
        confidence=_confidence_from_evidence(
            len(unique)
        ),
        evidence=evidence,
        metadata={
            "partial": bool(errors),
            "provider": provider,
        },
    )


def tool_fetch_sources(
    args: dict[str, Any],
    ctx: ToolContext,
) -> ToolResult:
    del ctx

    research = args.get("research")

    if not isinstance(research, dict):
        return ToolResult.failure(
            "'research' must contain "
            "web_research output"
        )

    sources = research.get("sources") or []

    if (
        not isinstance(sources, list)
        or not sources
    ):
        return ToolResult.failure(
            "No sources were supplied for fetching"
        )

    try:
        max_pages = int(
            args.get("max_pages", 4)
        )
    except (
        TypeError,
        ValueError,
    ):
        return ToolResult.failure(
            "'max_pages' must be an integer"
        )

    max_pages = max(
        1,
        min(max_pages, 8),
    )

    selected = sources[:max_pages]

    def fetch_one(
        source: dict[str, Any],
    ) -> tuple[
        Optional[dict[str, Any]],
        Optional[str],
    ]:
        url = str(
            source.get("url") or ""
        )

        if not _is_public_http_url(url):
            return (
                None,
                (
                    "Rejected non-public "
                    f"or invalid URL: {url}"
                ),
            )

        try:
            body, content_type = _text_request(
                url,
                timeout=20.0,
            )

            if content_type in {
                "text/html",
                "application/xhtml+xml",
            }:
                parser = _TextExtractor()
                parser.feed(body)
                parser.close()

                text = "\n".join(
                    parser.parts
                )

                title = (
                    " ".join(parser.title).strip()
                    or source.get("title")
                    or url
                )

            else:
                text = body
                title = (
                    source.get("title")
                    or url
                )

            text = re.sub(
                r"\n{3,}",
                "\n\n",
                text,
            ).strip()

            if len(text) < 120:
                return (
                    None,
                    (
                        "Insufficient readable "
                        f"content from {url}"
                    ),
                )

            digest = hashlib.sha256(
                text.encode("utf-8")
            ).hexdigest()

            return (
                {
                    "title": str(title),
                    "url": url,
                    "text": text[:12_000],
                    "content_type": content_type,
                    "sha256": digest,
                },
                None,
            )

        except (
            urllib.error.URLError,
            TimeoutError,
            ValueError,
            OSError,
        ) as exc:
            return (
                None,
                f"{url}: {exc}",
            )

    pages: list[dict[str, Any]] = []
    errors: list[str] = []

    worker_count = min(
        4,
        len(selected),
    )

    with ThreadPoolExecutor(
        max_workers=worker_count
    ) as pool:
        future_map = {
            pool.submit(
                fetch_one,
                source,
            ): source
            for source in selected
        }

        for future in as_completed(
            future_map
        ):
            try:
                page, error = future.result()
            except Exception as exc:
                page = None
                error = (
                    "Unhandled fetch worker error: "
                    f"{type(exc).__name__}: {exc}"
                )

            if page:
                pages.append(page)

            if error:
                errors.append(error)

    if not pages:
        return ToolResult.success(
            {
                "pages": [],
                "errors": errors,
                "used_search_snippets_only": True,
            },
            confidence=_confidence_from_evidence(
                len(sources),
                0,
            ),
            evidence=[],
            metadata={
                "partial": True,
                "fetched_pages": 0,
            },
        )

    evidence = [
        {
            "type": "fetched_page",
            "title": page["title"],
            "url": page["url"],
            "sha256": page["sha256"],
        }
        for page in pages
    ]

    return ToolResult.success(
        {
            "pages": pages,
            "errors": errors,
            "used_search_snippets_only": False,
        },
        confidence=_confidence_from_evidence(
            len(sources),
            len(pages),
        ),
        evidence=evidence,
        metadata={
            "partial": bool(errors),
            "fetched_pages": len(pages),
        },
    )


def _build_evidence_packet(
    research: dict[str, Any],
    fetched: dict[str, Any],
    max_chars: int = 45_000,
) -> tuple[
    str,
    list[dict[str, Any]],
]:
    sources = research.get("sources") or []

    pages_by_url = {
        page.get("url"): page
        for page in (
            fetched.get("pages") or []
        )
        if page.get("url")
    }

    normalized: list[dict[str, Any]] = []
    remaining = max_chars

    for index, source in enumerate(
        sources,
        start=1,
    ):
        page = pages_by_url.get(
            source.get("url")
        )

        content = (
            (page or {}).get("text")
            or source.get("snippet")
            or ""
        )

        maximum_for_source = min(
            8_000,
            remaining,
        )

        content = str(content)[
            :maximum_for_source
        ]

        remaining -= len(content)

        normalized.append(
            {
                "id": index,
                "title": source.get("title"),
                "url": source.get("url"),
                "content": content,
            }
        )

        if remaining <= 0:
            break

    encoded = json.dumps(
        normalized,
        ensure_ascii=False,
        indent=2,
    )

    return encoded, normalized


def _fallback_evidence_digest(
    goal: str,
    sources: list[dict[str, Any]],
) -> str:
    lines = [
        f"# Evidence Digest: {goal}",
        "",
        (
            "> An LLM was not configured. "
            "This is a source-grounded evidence "
            "index, not an AI-generated synthesis."
        ),
        "",
        "## Retrieved evidence",
        "",
    ]

    for source in sources:
        snippet = re.sub(
            r"\s+",
            " ",
            str(
                source.get("content")
                or ""
            ),
        ).strip()

        if len(snippet) > 600:
            snippet = (
                snippet[:597]
                + "..."
            )

        lines.extend(
            [
                (
                    f"### [{source['id']}] "
                    f"{source.get('title') or source.get('url')}"
                ),
                (
                    snippet
                    or "No readable excerpt was available."
                ),
                (
                    "Source: "
                    f"{source.get('url')}"
                ),
                "",
            ]
        )

    lines.extend(
        [
            "## Limitations",
            "",
            (
                "No model-based synthesis or semantic "
                "claim verification was performed. "
                "Review the linked sources before "
                "relying on this digest."
            ),
        ]
    )

    return "\n".join(lines).strip()


def tool_synthesize_report(
    args: dict[str, Any],
    ctx: ToolContext,
) -> ToolResult:
    goal = str(
        args.get("goal")
        or ctx.goal
    ).strip()

    research = args.get("research")
    fetched = args.get("fetched")

    if not isinstance(research, dict):
        return ToolResult.failure(
            "Missing or invalid research evidence"
        )

    if not isinstance(fetched, dict):
        return ToolResult.failure(
            "Missing or invalid fetched evidence"
        )

    evidence_json, sources = (
        _build_evidence_packet(
            research,
            fetched,
        )
    )

    if not sources:
        return ToolResult.failure(
            "No evidence was available for synthesis"
        )

    page_count = len(
        fetched.get("pages") or []
    )

    confidence = _confidence_from_evidence(
        len(sources),
        page_count,
    )

    source_evidence = [
        {
            "type": "source",
            "title": source["title"],
            "url": source["url"],
        }
        for source in sources
    ]

    if not ctx.llm.configured:
        digest = _fallback_evidence_digest(
            goal,
            sources,
        )

        return ToolResult.success(
            digest,
            confidence=confidence,
            evidence=source_evidence,
            metadata={
                "mode": "evidence_digest",
                "llm_used": False,
            },
        )

    system_prompt = (
        "You are an evidence-bound research analyst. "
        "Use only the supplied evidence. "
        "Never invent statistics, dates, entities, "
        "quotations, sources, results, or conclusions. "
        "Mark uncertainty explicitly. "
        "Cite every material factual claim with "
        "bracketed source IDs such as [1]. "
        "Do not cite a source unless the source "
        "actually supports the claim. "
        "Finish with a Sources section containing "
        "the source title and URL for every cited source."
    )

    user_prompt = (
        f"Research objective:\n{goal}\n\n"
        "Write a concise but substantive report with:\n"
        "1. Executive Summary\n"
        "2. Key Findings\n"
        "3. Evidence and Analysis\n"
        "4. Risks and Uncertainties\n"
        "5. Sources\n\n"
        f"Evidence packet:\n{evidence_json}"
    )

    try:
        text = ctx.llm.complete(
            system=system_prompt,
            user=user_prompt,
            temperature=0.1,
            max_tokens=3200,
        )

    except (
        RuntimeError,
        urllib.error.URLError,
        TimeoutError,
        ValueError,
    ) as exc:
        return ToolResult.failure(
            f"LLM synthesis failed: {exc}",
            retryable=True,
        )

    return ToolResult.success(
        text,
        confidence=confidence,
        evidence=source_evidence,
        metadata={
            "mode": "llm_synthesis",
            "llm_used": True,
            "model": ctx.llm.model,
        },
    )


