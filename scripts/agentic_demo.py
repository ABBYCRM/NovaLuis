def _normalize_sources(
    items: Iterable[dict[str, Any]],
) -> list[dict[str, Any]]:
    seen: set[str] = set()
    output: list[dict[str, Any]] = []

    for item in items:
        url = str(
            item.get("url") or ""
        ).strip()

        if not url:
            continue

        if url in seen:
            continue

        seen.add(url)

        output.append(
            {
                "title": str(
                    item.get("title")
                    or url
                ).strip(),
                "url": url,
                "snippet": str(
                    item.get("snippet")
                    or item.get("content")
                    or ""
                ).strip(),
                "provider_score": item.get("score"),
            }
        )

    return output


def _search_tavily(
    query: str,
    max_results: int,
) -> list[dict[str, Any]]:
    api_key = os.getenv(
        "TAVILY_API_KEY"
    )

    if not api_key:
        raise RuntimeError(
            "TAVILY_API_KEY is not configured"
        )

    response = _json_request(
        "https://api.tavily.com/search",
        method="POST",
        payload={
            "api_key": api_key,
            "query": query,
            "search_depth": "advanced",
            "max_results": max_results,
            "include_answer": False,
            "include_raw_content": False,
        },
        timeout=30.0,
    )

    return _normalize_sources(
        response.get("results") or []
    )


def _search_duckduckgo(
    query: str,
    max_results: int,
) -> list[dict[str, Any]]:
    encoded = urllib.parse.urlencode(
        {"q": query}
    )

    body, _content_type = _text_request(
        (
            "https://html.duckduckgo.com/"
            f"html/?{encoded}"
        ),
        timeout=25.0,
    )

    parser = _DDGParser()
    parser.feed(body)
    parser.close()

    return _normalize_sources(
        parser.results[:max_results]
    )


