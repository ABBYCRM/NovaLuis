def _is_public_http_url(
    url: str,
) -> bool:
    """
    Blocks localhost, private networks, link-local networks and malformed URLs.

    This limits basic SSRF exposure when the agent fetches search results.
    """

    try:
        parsed = urllib.parse.urlparse(url)

        if parsed.scheme not in {
            "http",
            "https",
        }:
            return False

        if not parsed.hostname:
            return False

        host = parsed.hostname.lower()

        if host in {
            "localhost",
            "localhost.localdomain",
        }:
            return False

        if host.endswith(".local"):
            return False

        try:
            addresses = socket.getaddrinfo(
                host,
                parsed.port or (
                    443
                    if parsed.scheme == "https"
                    else 80
                ),
                type=socket.SOCK_STREAM,
            )
        except socket.gaierror:
            return False

        for address in addresses:
            raw_ip = address[4][0]
            ip = ipaddress.ip_address(raw_ip)

            if not ip.is_global:
                return False

        return True

    except (
        ValueError,
        OSError,
    ):
        return False


