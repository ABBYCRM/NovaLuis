class _TextExtractor(HTMLParser):
    SKIP_TAGS = {
        "script",
        "style",
        "noscript",
        "svg",
        "canvas",
    }

    def __init__(self) -> None:
        super().__init__(
            convert_charrefs=True
        )

        self._skip_depth = 0
        self._in_title = False

        self.parts: list[str] = []
        self.title: list[str] = []

    def handle_starttag(
        self,
        tag: str,
        attrs: list[tuple[str, Optional[str]]],
    ) -> None:
        del attrs

        if tag in self.SKIP_TAGS:
            self._skip_depth += 1

        elif tag == "title":
            self._in_title = True

    def handle_endtag(
        self,
        tag: str,
    ) -> None:
        if (
            tag in self.SKIP_TAGS
            and self._skip_depth
        ):
            self._skip_depth -= 1

        elif tag == "title":
            self._in_title = False

    def handle_data(
        self,
        data: str,
    ) -> None:
        if self._skip_depth:
            return

        cleaned = " ".join(
            data.split()
        )

        if not cleaned:
            return

        if self._in_title:
            self.title.append(cleaned)

        self.parts.append(cleaned)


