def _json_request(
    url: str,
    *,
    method: str = "GET",
    headers: Optional[Mapping[str, str]] = None,
    payload: Optional[dict[str, Any]] = None,
    timeout: float = 25.0,
) -> Any:
    body: Optional[bytes] = None

    final_headers = {
        "User-Agent": (
            "NOVA-Agentic-MVP/1.0 "
            "(evidence-oriented research runtime)"
        ),
        "Accept": "application/json",
    }

    if headers:
        final_headers.update(headers)

    if payload is not None:
        body = json.dumps(payload).encode("utf-8")
        final_headers.setdefault(
            "Content-Type",
            "application/json",
        )

    request = urllib.request.Request(
        url=url,
        data=body,
        headers=final_headers,
        method=method,
    )

    with urllib.request.urlopen(
        request,
        timeout=timeout,
    ) as response:
        raw = response.read()

    return json.loads(raw.decode("utf-8"))


def _text_request(
    url: str,
    *,
    timeout: float = 20.0,
    max_bytes: int = 2_000_000,
) -> tuple[str, str]:
    request = urllib.request.Request(
        url=url,
        headers={
            "User-Agent": (
                "Mozilla/5.0 "
                "(compatible; NOVA-Agentic-MVP/1.0)"
            ),
            "Accept": (
                "text/html,"
                "application/xhtml+xml,"
                "text/plain;q=0.9,"
                "*/*;q=0.8"
            ),
        },
        method="GET",
    )

    with urllib.request.urlopen(
        request,
        timeout=timeout,
    ) as response:
        content_type = response.headers.get_content_type()
        charset = (
            response.headers.get_content_charset()
            or "utf-8"
        )
        raw = response.read(max_bytes)

    return (
        raw.decode(
            charset,
            errors="replace",
        ),
        content_type,
    )


