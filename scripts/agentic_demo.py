class SQLiteMemory:
    """
    Thread-safe, cross-run memory using SQLite.

    Stores complete JSON payloads rather than fake summaries.
    """

    def __init__(
        self,
        path: str | Path = ".nova_memory.sqlite3",
    ) -> None:
        self.path = str(path)
        self._lock = threading.RLock()

        self._conn = sqlite3.connect(
            self.path,
            check_same_thread=False,
        )

        self._conn.execute("PRAGMA journal_mode=WAL")
        self._conn.execute("PRAGMA synchronous=NORMAL")

        self._conn.execute(
            """
            CREATE TABLE IF NOT EXISTS episodes (
                id INTEGER PRIMARY KEY AUTOINCREMENT,
                run_id TEXT NOT NULL,
                key TEXT NOT NULL,
                value_json TEXT NOT NULL,
                created_at TEXT NOT NULL
            )
            """
        )

        self._conn.execute(
            """
            CREATE INDEX IF NOT EXISTS idx_episodes_key
            ON episodes(key)
            """
        )

        self._conn.execute(
            """
            CREATE INDEX IF NOT EXISTS idx_episodes_run_id
            ON episodes(run_id)
            """
        )

        self._conn.commit()

    def store(
        self,
        run_id: str,
        key: str,
        value: Any,
    ) -> int:
        payload = json.dumps(
            value,
            ensure_ascii=False,
            default=str,
        )

        created_at = dt.datetime.now(
            dt.timezone.utc
        ).isoformat()

        with self._lock:
            cursor = self._conn.execute(
                """
                INSERT INTO episodes (
                    run_id,
                    key,
                    value_json,
                    created_at
                )
                VALUES (?, ?, ?, ?)
                """,
                (
                    run_id,
                    key,
                    payload,
                    created_at,
                ),
            )

            self._conn.commit()

            return int(cursor.lastrowid)

    def recall_latest(
        self,
        key: str,
        default: Any = None,
    ) -> Any:
        with self._lock:
            row = self._conn.execute(
                """
                SELECT value_json
                FROM episodes
                WHERE key = ?
                ORDER BY id DESC
                LIMIT 1
                """,
                (key,),
            ).fetchone()

        if not row:
            return default

        try:
            return json.loads(row[0])
        except json.JSONDecodeError:
            return default

    def search(
        self,
        keyword: str,
        limit: int = 20,
    ) -> list[dict[str, Any]]:
        pattern = f"%{keyword}%"

        with self._lock:
            rows = self._conn.execute(
                """
                SELECT
                    id,
                    run_id,
                    key,
                    value_json,
                    created_at
                FROM episodes
                WHERE key LIKE ?
                   OR value_json LIKE ?
                ORDER BY id DESC
                LIMIT ?
                """,
                (
                    pattern,
                    pattern,
                    max(1, limit),
                ),
            ).fetchall()

        results: list[dict[str, Any]] = []

        for row in rows:
            try:
                value = json.loads(row[3])
            except json.JSONDecodeError:
                value = row[3]

            results.append(
                {
                    "id": row[0],
                    "run_id": row[1],
                    "key": row[2],
                    "value": value,
                    "created_at": row[4],
                }
            )

        return results

    def close(self) -> None:
        with self._lock:
            self._conn.close()


